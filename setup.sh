#!/bin/bash
# Simple setup.sh for configuring Ubuntu 12.04 LTS EC2 instance
# for headless setup. 

# Install nvm: node-version manager
# https://github.com/creationix/nvm
sudo apt-get install -y git
sudo apt-get install -y curl
wget -qO- https://raw.github.com/creationix/nvm/master/install.sh | sh

# Load nvm and install latest production node
source $HOME/.nvm/nvm.sh
nvm install v0.10.31
nvm alias default v0.10.31

# Install jshint to allow checking of JS code within emacs
# http://jshint.com/
npm install -g jshint

# Install rlwrap to provide libreadline features with node
# See: http://nodejs.org/api/repl.html#repl_repl
sudo apt-get install -y rlwrap

# Install emacs24
# https://launchpad.net/~cassou/+archive/emacs
# making *_proxy environment variables available for scripts 
# that are invoked through command sudo, because which remove 
# almost all of environment variables.
if [ -f ./setup/enableproxy.sh ]; then 
    . ./setup/enableproxy.sh
fi
sudo add-apt-repository -y ppa:cassou/emacs
sudo apt-get -qq update
sudo apt-get install -y emacs24-nox emacs24-el emacs24-common-non-dfsg

# Install Heroku toolbelt
# https://toolbelt.heroku.com/debian
wget -qO- https://toolbelt.heroku.com/install-ubuntu.sh | sh

# git pull and install dotfiles as well
cd $HOME
# backup dot files forcefully in dotfiles directory
if [ -f ./setup/backdots.sh ] ; then
    . ./setup/backdots.sh
fi
if [ -d ./dotfiles/ ]; then
    mv dotfiles dotfiles.old
fi
if [ -d .emacs.d/ ]; then
    mv .emacs.d .emacs.d~
fi
git clone https://github.com/nianjie/dotfiles.git
(
cd dotfiles
V_ENABLE_PROXY=$(git tag -l | grep -c v_enable-proxy)
if [ ${V_ENABLE_PROXY} -eq 1 ] ; then
   git checkout v_enable-proxy
fi
)
ln -sb dotfiles/.screenrc .
ln -sb dotfiles/.bash_profile .
ln -sb dotfiles/.bashrc .
ln -sb dotfiles/.bashrc_custom .
ln -sf dotfiles/.emacs.d .
ln -sb dotfiles/.vimrc

